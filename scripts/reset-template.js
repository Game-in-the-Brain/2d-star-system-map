/**
 * Pre-build script: reset standaloneTemplate.ts to a minimal placeholder.
 * This prevents the Vite bundle from accumulating nested copies of the
 * standalone HTML template across builds.
 */

import { writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const templatePath = resolve(__dirname, '../src/standaloneTemplate.ts');

writeFileSync(
  templatePath,
  `// Placeholder — regenerated by scripts/inline-build.js after vite build\nexport const STANDALONE_TEMPLATE = "";\n`
);

console.log('🧹 Reset standaloneTemplate.ts to placeholder');
